/**
 * Helper function to seed bank holidays into the database
 * Usage: Call this from a migration or seeder script
 */
export async function seedBankHolidays(bankHolidayRepository: any) {
  console.log('Seeding UK bank holidays for 2025-2026...');
  
  for (const holiday of UK_BANK_HOLIDAYS_2025_2026) {
    const existing = await bankHolidayRepository.findOne({
      where: {
        region: holiday.region,
        date: new Date(holiday.date),
      },
    });

    if (!existing) {
      await bankHolidayRepository.save({
        ...holiday,
        date: new Date(holiday.date),
        isActive: true,
        description: `${holiday.name} - ${holiday.region}`,
      });
      console.log(`✓ Added: ${holiday.name} (${holiday.region}) on ${holiday.date}`);
    }
  }

  console.log('✓ Bank holidays seeded successfully!');
}
